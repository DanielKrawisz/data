    template <typename N> void power_mod_test_case (N mod, N base, N exp, N expected) {
        EXPECT_EQ ((data::pow_mod<N> (base, exp, nonzero {mod})), expected) <<
            "expected " << base << " ^ " << exp << " % " << mod << " == " << expected;
    }

